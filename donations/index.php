<h1>Donations/Gifts</h1>

	<h3>Gift Policy</h3>

		<p>Benner Library appreciates and encourages gifts to its collection with the understanding that all gift items are used in the most beneficial manner. The acceptance of any donation is contingent on whether or not it supports the Libraryâ€™s mission and collection development criteria.</p>
        <p>Prospective donors should be aware of the following policy statements:</p>
			<ul class="padding10-left no-margin-top">
				<li>Since all items generate processing and storage costs, no gift item will be added unless it will enhance the intellectual value of the collection.</li>
        <li>It is the responsibility of the donor to arrange for transportation of the gift.</li>
				<li>The Library reserves the right to dispose of any unneeded publications. Once donated, materials cannot be returned to the donor.</li>
				<li>If there is a question about accepting a gift, the final determination rests with the Dean of Library Services.</li>
				<li>Space limitations preclude any agreement to maintain a gift collection as a separate entity.</li>
				<li>Acceptance of museum materials is contingent upon available space and the following factors:  significance for Olivet, association with book materials, and ease and cost of retention.</li>
			</ul>


		<p>Benner Library can provide a gift acknowledgement letter for donors who request one, provided the donor completes a <a id="pdf" href="/donations/docs/donation_form.pdf" target="_blank">Donation of Library Materials Form</a>. However, IRS regulations do not allow an institution to assign a monetary value to gifts received.  The donor is responsible for any estimates or appraisals to be used for tax purposes and any estimate or appraisal must be completed prior to the delivery of materials to the library.  Donations valued at $5,000 or more require documentation from a qualified appraiser.</p>
