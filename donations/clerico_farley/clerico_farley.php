<h1>Clerico-Farley Donation</h1>
<img style="float: left; margin: 5px 15px 5px 15px;" src="/donations/img/clerico_farley2.jpg" alt="Picture of Irene Clerico Farley" border="2" />
<p>In January 2016, a generous monetary gift was given to Benner Library from Robert Norman Farley in memory of his wife Irene Clerico Farley ’47.</p>
<p>Irene was raised in Flint, Michigan, the only daughter in an Italian immigrant family. Her mother valued education above anything else and made sure her children had an opportunity for a college education. Irene followed her brother, John, to Olivet where he was actively engaged in the academic experience. She thrived at Olivet and was involved in many campus activities, including: International Relations Club, Platonian Philosophical Society, Chi Sigma Rho, Glimmerglass, Linguistic Society and “O” Club. In addition to active memberships, she also held leadership positions in campus clubs. She worked throughout her time at Olivet in the library as an intern. Irene graduated with a Bachelor of Arts in English in 1947 and then returned the following year to complete her teaching credential and a Bachelor of Science degree.</p> 
<img style="float: right; margin: 5px 15px 5px 15px;" src="/donations/img/clerico_farley.jpg" alt="Picture of Irene Clerico Farley" border="2" />
<p>In 1949, Irene married Robert Farley, a fellow Olivetian, and the two moved to Sacramento, California in 1954. Irene had a lifelong career as a librarian in a number of different institutions—University of Pacific McGeorge School of Law; Sacramento City College; and the Sacramento City and County library system. After years of service and raising her three daughters with her husband, Robert, Irene passed away to meet the Lord in the summer of 2015. She was 90 years old.</p>  
<p>Benner Library will use the donation to create the Irene Clerico Learning Zone (ICLZ). The learning zone will be located in the Curriculum Center on the second floor of Benner Library and will include 24 Steelcase Node chairs, a large screen TV, a book binder, and movable whiteboards. A 3D printer and a poster/plotter printer will also be purchased and eventually moved to the Learning Zone.</p>

<p>To enjoy the progress of project visit the <a href="http://libraryinformatics.blogspot.com/" target="_blank">Benner Library Informatics Blog</a>.</p>
		



