<h1>Dr. William Foote</h1>
	<img style="float: left; margin: 5px 15px 5px 15px;" src="/donations/img/foote.jpg" alt="Picture of Dr. William Foote" border="2" />
	<p>
		Dr. William Foote, a professor of English at Olivet Nazarene University from 1967-1995, died on November 29, 2012.  Through his estate, he left his personal library of over 3,000 books to the University and, ultimately, to Benner Library.  Anyone who knew Dr. Foote also knew that he loved books.  A quote from his <a href="http://www.lifestorynet.com/obituaries/william-foote.85129" target="_blank">obituary</a> highlights this passion:
			<blockquote>
			“In his free time, Bill enjoyed reading, photography, music, conversation and traveling. He and [his wife] Reu were avid travelers, exploring destinations including Hawaii, Europe, and the Middle East. He loved reading, and often browsed through area bookstores. In fact, the Barnes & Noble employees liked to say that Bill had every book they ever sold!”
			</blockquote>
	</p>

	<p>
		Dr. Foote possessed a wide range of interests and hobbies and his library certainly reflected that. His collection included works on religion, philosophy, U.S. and World history, politics, travel, art, music, language, and literature.
	</p>

	<img style="float: right; margin: 5px 15px 5px 15px;" src="/donations/img/foote2.jpg" alt="Picture of Dr. William Foote" border="2" />

	<p>
		Benner Library staff carefully reviewed and evaluated Dr. Foote’s library and added
		<a id="pdf" href="/donations/docs/foote_books.pdf" target="_blank">over 1,900 volumes</a>
		to the University’s collection—either as new titles or as replacements for items in poor condition. Books that were not added were included in multiple library book sales.  The sales of beautiful volumes from Dr. Foote’s collection were a big hit with the campus community and generated revenue for Benner Library to invest in further resources.
	</p>





