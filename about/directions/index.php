    <h3>Parking</h3>
        <p>There are four major parking lots on campus for visitors:<br />
		<a href="https://www.olivet.edu/campus-map" target"_blank">See campus map</a>.
		<ol>
			<li><strong>Larsen/Reed Parking</strong> is accessible from Main Street, between University Avenue and the Highway 102 intersection at the light</li>
			<li><strong>Centennial Chapel West Parking</strong> (Ludwig) is easily accessible from University Avenue, the main entrance to campus from Illinois Highway 45</li>
			<li><strong>Weber Parking</strong> is north-east of most of the campus buildings</li>
			<li><strong>Centennial Chapel East Parking</strong> (Ludwig) is directly east of the Betty and Kenneth Hawkins Centennial Chapel</li>
		</ol>
	<h3>From Larsen/Reed to Benner</h3>
        <p>From the Larsen/Reed parking lot, walk through the Larsen Fine Arts Center, across the Esplanade, past the Strickler Planetarium and south end of Chapman Hall and up the ramp to the front entrance of the library. Upon arriving, you may ask for help at the Circulation Desk immediately to your left as you enter, or at the Reference Desk, which is straight ahead, near the elevator.</p>

