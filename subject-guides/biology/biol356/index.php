	<h5>Method 1: Use <strong>Summon Advanced Search</strong> to find online (full text) articles</h5>
		<ul class="margin50-left">
			<li>Go to the <a href="/index.php">Benner Library homepage</a></li>
			<li>Click on the “Journals &amp; Articles” tab</li>
			<li>Click on the “Advanced Summon Search” link</li>
			<li>Set your search limits: </li>
				<ul>
					<li><strong>Change the first drop-down menu to “ISSN”</strong> and type the unique ISSN for the journal you desire into the search box (use 0028-0836 for <em>Nature</em>)</li>
					<li>Type relevant keyword(s) in the second search box, such as <strong>microbiology</strong></li>
					<li>Change the date range, if needed</li>
				</ul>
			<li>Click on the “Search” button</li>
		</ul>

	<h5>Method 2: Using the <strong>Journal Search (by title or ISSN)</strong> to find online (full text) articles</h5>
		<ul class="margin50-left">
			<li>Go to the <a href="/index.php">Benner Library homepage</a></li>
			<li>Click on the “Journals &amp; Articles” tab</li>
			<li>Click on the “Journal Search (by title or ISSN)”</li>
			<li>Search for the journal title e.g. <em>Nature</em>. </li>
			<li>Look for the title you desire e.g. <em>Nature (London)</em>.</li>
			<li>Click on the database which contains the full text for the dates you desire.  If there are several options, the databases are listed in preferred order by Benner librarians.</li>
			<li>Use a tool on the journal holdings page</li>
				<ol>
					<li>If you have an area of interest, search within the journal by keyword</li>
					<li>If you know the date, browse by date/issue</li>
				</ol>
		</ul>

