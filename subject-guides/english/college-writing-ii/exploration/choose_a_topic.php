<h4>Actions you can take</h4>
	<ul>
		<li>Talk with a <a href="/contact/ask-a-librarian.php">librarian</a>
		<li>Make a <a href="http://cmap.ihmc.us/docs/constructingaconceptmap.php" target="_blank">concept map</a> to help organize your ideas and background information
			<ul>
				<li>Construct a focus question (what problem or issue do you want the concept map to help resolve?)</li>
				<li>Identify key concepts</li>
				<li>Rank order the concepts</li>
				<li>Construct an initial concept map using linking words like, “provides, have, in order to, ”</li>
