<h2 class="header-line">Choose a topic</h2>

	<p>Begin with an area that interests you.  Ideally, you will chose a topic from your major, so that you will get experience using the databases for your academic discipline.<br /><br />
	Defining a topic is actually one of the most challenging phases of the research process. It involves:</p>
	<ul>
		<li>Identifying and selecting a general topic</li>
		<li>Exploring, by conducting background research</li>
		<li>Narrowing &amp; clarifying your topic</li>
	</ul>
