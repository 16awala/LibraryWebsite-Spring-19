<h2 class="header-line">Writing Tips</h2>

<h4>Read &amp; Take Notes</h4>
	<p>Conventional wisdom tells us, "<strong>A dull pencil is better than the sharpest memory</strong>."</p>
	<p>For the College Writing II research project, you’ll be considering so many different sources and different kinds of information that it will be hard to keep all of the details straight without notes.  You’ll probably need a lot of detailed notes!  Therefore, </p>
	<ul>
		<li class="bold no-margin-bottom">Read and take notes</li>
		<ul class="no-margin-top">
			<li>Reading, but not taking notes?  You're likely not reading carefully enough.</li>
			<li>Reading carefully and not taking notes?  You’ll likely be disappointed to discover that it's difficult to remember what you found and where it came from when it’s time to write.</li>
		</ul>
		<li>Consider taking electronic notes.  They are easy to search later.</li>
		<li>Some students prefer to create physical notes, organized by topic or source.  Use a system that works well for you!</li>
	</ul>
	<h4>BEAM Model</h4>
	<p>Joseph Bizup developed the BEAM Model to describe how various sources might be used in a paper. </p>
	<ul>
		<li>Some sources will provide <strong>background</strong>.</li>
		<li>“<strong>Exhibits</strong>” are primary sources that a humanities writer (MLA, CMS) may analyze.</li>
		<li>Students in all disciplines will become familiar with “<strong>argument</strong>” sources which are used as authoritative examples of the position an author is taking.</li>
		<li>Finally, some advanced scholars use <strong>method</strong> sources for the theoretical framework they embody.</li>
	</ul>

