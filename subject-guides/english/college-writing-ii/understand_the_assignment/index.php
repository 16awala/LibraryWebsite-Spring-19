<p>Different academic disciplines have different writing conventions, based on the type of research questions they ask and the approach they use for investigation.  The College Writing II courses are designed as an introduction to the thinking and approach of your intended major, not only as a class to teach you how to format citations! In connection with this, you will gain experience using the scholarly article databases for your intended academic area.</p>

<p>Mercer University Libraries provide an excellent summary of the reason for various writing styles <a id="pdf" href="https://libraries.mercer.edu/research-tools-help/citation-tools-help/DifferentCitationStylesHandout.pdf" target="_blank"><img src="/img/get-pdf.png" /></a>
