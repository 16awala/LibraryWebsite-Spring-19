		<h3><a name="themes" id="themes"></a>4. Identifying the Major Emphases or Themes</h3>
		<p>It is important to find what themes or emphases the author is trying to convey. These motifs often shape the author's intended meaning. For instance, Matthew strongly emphasizes how Jesus fulfilled the Mosaic Law and was Israel's true Messiah. Therefore, the writer of Matthew quotes many Old Testament passages to show how Jesus fulfilled prophecies. The writer also uses the genealogy of Jesus to show the connection to King David's line - the line from which the Jews believe the Messiah would come (8).<br /></p>

