		<h3>3. Identifying the Literary Context </h3>
		  <p>The literary context refers to the framework surrounding your passage.  Literary context involves understanding where in the progression of the book the passage is located (from beginning to end).  Here's an analogy:  think of your book as a large timeline with each passage being a different event on the timeline.  Suppose one of the events on this timeline was the Roman sacking of Jerusalem in 70 AD.  You could easily study and read a wealth of information about that particular event, but your understanding of the event would increase if you also paid attention to the context of the fall of Jerusalem.  What events led to the Roman assault on Jerusalem?  How did the sacking of Jerusalem affect the Jews in future events?  Answers to these questions form the context that shapes your understanding of this one historical event. <br /></p>
	<br />
	<p><strong>Broad Literary Context</strong><br /><br /></p>
	<p>The broad literary context refers to the overal framework that the passage fits in.  This would be like looking at our one event in relation to the rest of history.  How far have we traveled in history? How does our one event important to the whole of history? The broad literary context also tends to see how your passage fits in with the overall themes of the book presented throughout the passage.  Someone looking at the broad literary context for the passage that tells the story of Christ's birth in the gospel of Luke would need to pay attention to the themes Luke presents about Christ and how this birth impacts and participates with the rest of Luke's account of Christ's life. </p>
	<br />
	<p><strong>Immediate Literary Context</strong><br /><br /></p>
	<p>The most important part of the literary context are the texts that immediately  surround your passage. These passages provide the most information relevant to your passage's meaning and importance. In our timeline analogy, the immediate context could tell why the Romans assaulted Jerusalem. The immediate context could also tell you how Jerusalem's fall impacted the Jewish/Christian relationships that the New Testament writers had to address . The same could be true in an exegetical study.  For instance, if someone were writing
		  an exegesis on the beatitudes in Matthew, it would be very important
		  to recognize that the beatitudes opens the Sermon on the Mount and specifically fits the context of Jesus discussing what it means to be a disciple (7). <br />
		  <br /></p>
		  <p><strong>Sources for Finding the Literary Context</strong></p>
		  <ul>
		  <li><a id="pdf" href="/subject-guides/religion/exegesis/doc/resources-list.pdf#page=2" target="_blank">Bible Commentaries</a>: Commentaries sometimes provide connections from your passage to previous or future passages.  The New Interpreter's Bible is an excellent source for finding information on the literary context.</li>
		  <li><strong>Bible</strong>: Sometimes your own personal reading of the surrounding Scripture passages can be the best way to find the literary context.  See if your passage follows similar themes as its neighboring passages.  If you are exegeting a narrative passage, watch for changes in setting or characters.</li>
