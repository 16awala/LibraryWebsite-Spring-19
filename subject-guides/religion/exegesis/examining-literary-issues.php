		<h3>Identifying the Literary Genre and Subgenre </h3>
		<p>So far you have chosen a topic and researched the introductory information for your exegesis. The next step is to learn about the literary background of your passage. This step is very important because literary issues shape the meaning of the passage. The genre, subgenre, and organizational structure of the passage give hints to the overall direction and pattern that the passage is following. For example, Paul's letters generally follow (but not with perfect consistency) a pattern. The genre, subgenre and structure of the passage will help you understand this pattern, enabling you to understand what Paul originally intended to say to his audience. </p>

		<h3>1. Genre</h3>
		 <p> The term &quot;genre&quot; in biblical criticism often refers to the German word &quot;gattung&quot;, which is used to mark a group of works for having &quot;important or distinguishing characteristics in common&quot; (1). The genre &quot;involves the form, style, and subject matter which sustains and dictates a complete literary work&quot; (2).Genre is important because it impacts the way we interpret the text. Michael Gorman, a biblical scholar, provides a great modern day illustration. If you picked up a newspaper, you would notice that the front page news, the comics and the editorials are all different types of literature and must be interpreted in a way that fits each type. You would not want a world news report to exaggerate something. However some exaggerations are expected in advertisement. Similarly, people do not read the Sunday comics in the same way they read obituaries.  In the same way,  you need to understand the type of literature (or genre) of the book in which you find your passage (3). You cannot interpret a passage from a Gospel in the same way as an Epistle. The following are some basic genres that might fit the book in which you find your passage: (4) </p><br />

