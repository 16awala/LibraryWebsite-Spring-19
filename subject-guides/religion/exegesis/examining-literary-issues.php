		<h3>2. Subgenre </h3>
			<p>Genre and subgenre are very similar to each other, but while the genre refers to categories that affect the entire work, subgenres refer to literary types that do not affect the entire work.  In the case of your exegesis, the subgenres still affect your passage  (5). Like the genre, the subgenre is important to understand because passages which have unlike literary forms should be approached differently. For instance, you would not approach Hebrew poetry from Lamentations in the same way that you would approach an apocalyptic vision found in Daniel or Revelation. To approach them in the same way would lead to wrong conclusions about  the author's meaning. Here are a few possible subgenres or literary forms you might encounter: (6) <br /></p>
