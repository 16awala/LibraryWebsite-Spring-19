	<h3>Tips</h3>
	<p>
	<strong>It is very important to look for this introductory information in multiple books.</strong> Each author typically has their own opinion about introductory issues. For instance, some commentaries may  say that some of the letters attributed to Paul (like Colossians) were not actually written by him. (1) Others may follow the more traditional belief that all of the letters were written by Paul.
	<br /><br />
	It is also good to include several different theories regarding the introductory information. You do not want to make this the longest part of your exegesis, but you still need to show that you are aware of the different theories relating to introductory items. Including two or three possible authors, for example. may be a good idea as long as it does not take up too much space.
	<br /><br />
	A final tip is that you may wish to use some of this introductory material in the body of your exegesis as well. This is fine as long as you make sure that you are not weakening your introduction. </p>

