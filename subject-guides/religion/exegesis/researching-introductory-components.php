    <h3>What are Introductory Components?</h3>

	<p>Introductory Components are elements that relate to the book or letter you are investigating. This is very important because these elements will shape the meaning of your passage and  will help you understand  the true meaning of your passage. For instance, if someone was studying  the book of Revelation, it would be wise for him/her to research when the book was written. What Roman emperor was reigning when Revelation was written? What historical events had taken place that might effect the interpretation of the letter?  Knowing the date the document was written will inform your perspective of the meaning of Revelation possibly from looking at it as something that will happen, something that has happened, or something that is happening.</p>

	<p>The following are some key introductory components that you should research:</p>

