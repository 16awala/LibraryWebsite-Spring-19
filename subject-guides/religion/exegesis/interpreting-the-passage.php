<h2>Wrapping Up the Research</h2>
<p>You have accomplished a lot of research by now.  You have already</p>
<ul>
  <li>Selected your passage </li>
  <li>Read your book and your passage multiple times in different translations</li>
  <li>Found information on introductory components (authorship, date written, audience, etc.)</li>
  <li>Identified the genre, subgenre, and structure </li>
  <li>Read about major themes in the book to which your passage may apply</li>
  <li>Identified parallel passages</li>
  <li>Completed several word studies</li>
  <li>Finished a verse-by-verse analysis</li>
</ul>
<p>It is now time to take all of this information and combine it into manageable, cohesive interpretation(s) of the passage.  Your final step will be to apply the meaning of the passage to the overall themes of Scripture and to real life</p>

<h3>Encapsulating Your Passage - Writing the Focal Point</h3>
<p>Reread all of the information you have gathered.  Watch for relationships between each type of information that you have discovered.</p>

<p><strong>1. Start from the beginning with your information about the book.</strong></p>
  <ul>
    <li>Review issues of authorship, date, etc. from your <a href="/subject-guides/religion/exegesis/researching-introductory-components.php">Introductory Components</a>, keep your passage specifically in mind.</li>
	<li>Go over what the genre of the book was</li>
	<li>Re-examine the major themes and emphasis found in your book that are related to your passage.</li>
	<li>Try to understand the overall context of your book.</li>
  </ul>

<p><strong>2. Look at information specific to your passage and to your verses</strong></p>
  <ul>
    <li>Look at the subgenre and structure of the passage.</li>
	<li>Re-analyze the flow of thought of your passage from your verse-by-verse analysis and see how it fits into the genre, subgenre and structure</li>
	<li>Go over the main points from your verses and note how they fit into the context of the original audience</li>
	<li>Review your word studies and make sure the meanings fit your passage</li>
	<li>Reassess your parallel Biblical passages and make sure they still are parallels, now that you have a better understanding of your passage. Do they still help interpret your passage?</li>
</ul>
<p>Now that you have reviewed all of your material and seen how the individual pieces relate to each other, write the basic meaning(s) of the passage and why the author originally wrote this to his audience.  These sentences are the focal point of your exegesis, like a thesis.<br /><br />
Compare the focal point you just wrote with your responses to the questions on the <a id="pdf" href="/subject-guides/religion/exegesis/doc/read_passage_worksheet.pdf" target="_blank">Read the Passage worksheet</a>  after you read your passage for the first time.  Are there similarities?  Are there differences?  Reflecting on your original opinions as compared to your research-based understanding will help you when you apply the passage, since you can now express what portions of the passage is difficult to understand with just a surface level reading.</p>


