<h3>2. Reading The Entire Passage</h3>
	<p>
		After you have selected your passage, the next step is to <strong>read the entire book or letter</strong> in which your passage is located. The main idea of this exercise is to start fitting the passage's meaning with the rest of the book or letter, not to skim the book to meet class requirements.<br />
		During your reading, you might want to take notes on what you are reading. <strong>Below is a list of example questions you may want to see if you can answer.</strong>
	</p>

	<ul>
		<li>Can you identify any special emphases or concerns in the book?</li>
		<li>What do you think the purpose of this book is?</li>
		<li>Who are the recipients of the book? Are they Jews? Gentiles? Both?</li>
		<li>Do the recipients have a relationship with the author?</li>
		<li>What kind of relationship?</li>
		<li>What do you think the meaning of the passage is?</li>
	</ul>


	<p>Write out any more questions specific to your book or passage that you thought about.  This will help identify some of the issues you will want to address through your research and in your paper.</p>

