	<h3>1. Choosing a Scripture Passage</h3>
	<p> Choosing a Scripture passage is one of the most important parts of an exegesis. Make a thoughtful choice from the list of passages that your professor provides. What passage are you curious about? Which one sticks out in your mind so that you wonder whether there is something deeper than what you are reading? Remember, you're going to be spending a lot of time looking at these verses, so you should choose one in which you have a personal interest. <br /><br />This table is set up to provide some <strong>tips</strong> if you are exegeting a passage from the New Testament:</p>

