	<p><strong>Considerations  About Sources</strong><br /></p>
	<p>There are some basic considerations that you must be aware of when finding/using/selecting sources.
	</p>
		<ol>
			<li>Be careful of works older than 1950 because archeological material (which contributed both historic and linguistic information) was not widely available until after the end of World War II (3).</li>
			<li>Be aware that each scholar is coming from his/her own theological foundation. For example, what does their tradition say about free will?  The role of women in leadership roles? Etc.  A scholar's tradition impacts how he/she views your passage and the significance of each verse.  Reading multiple sources will help you avoid the bias of a particular tradition (4).</li>
			<li>Be aware that a scholar might have misinterpreted something.  Although this is unusual, do not assume that a scholar has the complete picture of what the verse was intended to mean just because he/she published a commentary.  Scholars may be experts in their field, but they still can make mistakes.</li>
		</ol>
