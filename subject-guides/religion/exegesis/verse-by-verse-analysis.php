	<h3>What is a Verse-By-Verse Analysis</h3>
		<p>A verse-by-verse analysis is a commentary on a passage that is written one verse at a time.  Most published commentaries are set up using verse-by-verse analyses.  These analyses are written by listing each verse in the passage then writing all of the information that applies to that verse.  Putting the information together in this way makes it easier for the reader to have a clear picture of what the verse was originally meant to say.</p>
		<p>Although the most appropriate way to write a verse-by-verse analysis is by breaking down the passage one verse at a time, it is not the best way to research information for each verse.  If you were to try and research each verse individually before moving on to the next verse, an exegesis could easily take forever and you would not be able to follow the flow of the scholars' interpretations (1)!  Instead, you should research one source at a time.  To do this, choose one good source to start with and look up all the information on the verses in your passage. Take notes on each verse.  NOTE: Make sure that you still write your exegesis with a verse-by-verse analysis, not source-by-source.</p>
		<p>Use multiple sources in order to adequately explain each verse.  As you will discover, scholars do not always agree with each other.  Therefore, it is important for you to be aware of the majority view on a particular verse and passage and to consider the other perspectives.  Usually the true meaning of the verse lies in combining the perspective of several sources. This is because most writers focus on only one or two main aspects (such as a historical or a literary view point) of a passage.  It's up to you to combine the scholars' work.  Estimate that 50-60% of your total sources for the paper will come from verse-by-verse analysis.</p>

	<h3>What to Look for in a Verse.</h3>
		<p>When you begin looking through the individual verses, you need to write down everything significant that the author says.  Additionally, there are a few key areas that you definitely need to cover:</p>

	<h5>Word Choice and Writing Styles</h5>
		<p>One of the important things to pay attention to is how the Biblical author uses his words.  Try to uncover how the author originally wanted this passage to be read.</p>
		<ul >
	  		<li>Is the Biblical author emphasizing a particular word?</li>
	  		<li>Is the way the Biblical author phrases his sentences unique?</li>
	  		<li>Is the Biblical author's writing sarcastic? Rhetorical? Encouraging?</li>
		</ul>
	<h5>Cultural and Historical References</h5>
		<p>Cultural and historical references are often hidden beneath the words on the page because the author assumes the reader already knows about the event or theme.  Since we don't live in ancient Jerusalem or Rome, you must try and discover what cultural references the author might be using.</p>
		<ul>
	  		<li>Does the Biblical author refer to any cultural practices?  What does he say about them?</li>
	  		<li>Does the Biblical writer refer to any other writings that the original readers would have read?</li>
	  		<li>Are there any unusual teachings being introduced to the audience?</li>
		</ul>
		<p>Try to find out which historical references the writer was thinking of.
		</p>
		<ul>
	  		<li>Does the passage refer to any historical events that the reader would have been aware of?</li>
	  		<li>Does the Biblical writer refer to his personal life or any previous/upcoming interactions with the original reader?</li>
		</ul>
	<h5>Similarities and Differences Between Other Verses and Other Manuscripts</h5>
		<p>Like you did when <a href="/subject-guides/religion/exegesis/examining-literary-issues.php">Examining Literary Issues</a>, it is important to compare/contrast the verse in your passage with other verses in the book and in the Bible.</p>
			<ul>
	  			<li>Does the Biblical scholar point out any parallel verses (in theme or structure)?</li>
	  			<li>Does the Biblical scholar note other verses that emphasize the same idea or a contrasting thought.</li>
			</ul>
		<p>Watch out for variances between different manuscripts.  Before everything was combined into one book of the Bible through a series of meetings, debates, and councils, (2) there were (and still are) thousands of copies of manuscripts of nearly the same Scripture.
		</p>
			<ul>
			  <li>Record any differences between various Biblical manuscript copies. NOTE: One of the best places to look for these differences is in the footnotes of the Bible (e.g. &quot;some manuscripts say.&quot;). </li>
			  <li>If there are differences, which version makes the most sense given your research?</li>
			</ul>
	<h5>Significance to the Passage</h5>
		<p>All of the preceding steps contribute to this step.  <strong>The most important thing to do when looking at individual verses is to pay attention to where the verse fits contextually in the flow of thought for the passage.</strong></p>
		<ul>
			  <li>Is the verse positioned as the climax of a section or passage?</li>
			  <li>Does it present a significant idea or provide a foundation for one?</li>
			  <li>Does the verse explain the meaning of other verses?  Does it apply the meaning of another verse to life?</li>
		</ul>
	<h3>Sources for Verse-By-Verse Analysis</h3>
	<p><a id="pdf" href="/subject-guides/religion/exegesis/doc/resources-list.pdf#page=2" target="_blank">Commentaries</a>: Commentaries will be your best source for finding verse-by-verse information.  The reason for this is that commentaries are set up similar to a verse-by-verse analysis.  The bulk of a commentary is dedicated to <em>commentating</em> on Biblical book verse-by-verse.</p>

	<p>Journal Articles: Journal articles are useful for finding more specific information about verses.  They will usually provide a different perspective about your verses than what traditional scholars will discuss.  The difficulty about journal articles is that they typically are much harder to read since they are written with a academic audience in mind. If you have trouble reading a particular article, move on to another article that is easier to understand.
