	<h3>How to Begin a Word Study</h3>
	<p>After you have selected a word that you want to understand better, the next step is to begin the word study.  Here are some ideas on how to get started:</p>
	<h5>1. Find Other Verses That Use the Same Word</h5>
		<p>There are several ways of finding other verses:</p>
		<ol>
		  <li>Use a concordance. It will give you every occurrence of the English version of your word.  Be very careful though - just because the same English word is used doesn't mean that the same Greek or Hebrew word was! </li><br />
		  <li>Use <em>The Word Study New Testament</em> by Winter and Winter (2) in conjunction with <em>The Word Study Concordance</em> by Wigram and Winter (3) or another set of books like these. The <em>Word Study New Testament</em> provides an English copy of the New Testament. Underneath major words is a number system.  If you match the number of the word to the same number listed in <em>The Word Study Concordance</em>, you will find the Greek word that is used in that verse along with every other occurrence of the Greek word in the New Testament.  These books are very useful sources.  Note: it is not wise to rush through this process and just write down the first three verses that use the word.  You need to find out how the word actually used.  Take some time to find verses that seem appropriate to the context of your passage.</li>
	</ol>
	<h5>2. Find the Definition of the Word</h5>
		<p>There are several ways of finding other definitions:</p>
		<ol>
			<li>To find the most in-depth definition use a Greek-English or Hebrew-English lexicon.  However, these types of books can be difficult for beginners to use since most are organized according to the Greek or Hebrew alphabet.  <em>The Greek Word Study Guide</em> and <em>Hebrew Word Study Guide</em> can help you find the correct word if you have already seen what the Greek word looks like.</li>
			<li>Another way to find the definition is to use sources like A.T. Robertson's <em>Word Pictures in the New Testament</em> (4) or Vine's <em>Expository Dictionary of New Testament Words</em> (5).  These books use the English words but explain what the actual original Greek meant.  Make sure you remember what your verse reference is, since this will be very helpful when using these sources.</li>
		</ol>

	<h3>(Advanced Step) How is the Word is Used</h3>
	<p>The preceding steps help identify the root meaning of the word.  If you have training in Greek and Hebrew, see if you can also parse the word.</p>
	<ul class= "padding15-left">
	  <li>Which part of speech is the word?</li>
	  <li>What is the mood of the word?</li>
	  <li>What is the word tense</li>
	  <li>Is the word singular or plural</li>
	  <li>Is the noun nominative? Accusative? Etc.</li>
	</ul>

	<p>Analytical lexicons like William D. Mounce's <em>The Analytical Lexicon to the Greek New Testament</em> (6) are excellent sources for parsing.</p>

	<h3>Apply What You Found</h3>
	<p>Now that you have all of this information about a particular word, reread the verse with your new understanding.  This will accomplish two things.  First, you will find new insight into your verse, since you now have a better understanding of what is being said.  Second, make sure that your definition makes sense contextually with your verse.  If the meaning you have found does not match what the verse is saying, then you may have to do more research to find out the original meaning.  If you become too frustrated because of this, make a note of the problem area and move on to the next word or to the verse-by-verse analysis.  Sometimes understanding other key parts of the passage can inform the meaning of the word that is difficult to understand.</p>

	<h5>Sources for Word Studies</h5>
	<p>For recommended titles and call numbers see the<br />
	<a id="pdf" href="/subject-guides/religion/exegesis/doc/exegesis_guidelines.pdf" target="_blank">Scriptures II Exegesis Guidelines <img src="/img/get-pdf.png" /></a></p>
	<ul>
	  <li>Language and Word Studies Books: This assortment of books cover definitions, proper Greek or Hebrew grammar, interlinear translations of Scripture, and much more.  </li>
	  <li>Lexicons: Lexicons provide the parsing information along with a definition of the root word. However, most are alphabetically arranged to either Greek or Hebrew. </li>
	  <li>Concordances: Concordances provide lists of Scripture that use the same English word in that translation. Note: This does not necessarily mean that the Scriptures are discussing the same things-- even though the same English word is used. If you use a concordance, make sure you read the other Scripture in its context CAREFULLY!</li>
	  <li>Bible Translations: Bible translations easily show where scholars have interpreted a word, phrase, or verse differently.  This can give insight into a word's meaning since most interpretations are not entirely right or wrong but draw on a particular use of a word.</li>
	</ul>
	<h3>Tips</h3>
	<p><strong>Note What Commentators Write</strong><br />
	Unfortunately, you can not use this method until after you start the verse-by-verse analysis.  However, remember when you start your verse-by-verse analysis. Pay attention when scholars make comments about the meaning of particular words.  Their statements can provide insight into a word, especially if that particular commentator connects the meaning to his/her interpretation of the verse and passage.  Some sources have multiple definitions of the same word.  Make sure you find the ones most appropriate to your passage.</p>

