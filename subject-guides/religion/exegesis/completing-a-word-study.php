	<h2>Difficulties of Word Studies</h2>
	<p>One of the most difficult tasks of an exegesis is finding the actual definition of a word.  Although it would seem easy just use a dictionary, the problem is <strong>you must find the meaning of the original Greek or Hebrew word</strong>.  Unfortunately for general education students, you probably have never been exposed to Greek or Hebrew.  But don't worry; there are lots of sources created to help people with word studies.</p>

	<h3>Which Words are Important Enough for a Word Study?</h3>
	<p>Identifying which words need to have a word study can be difficult.  Luckily, you already have some information to help you decide.  Remember when you noted the differences between translations of key words?  Your list from that <a id="pdf" href="/subject-guides/religion/exegesis/doc/word_study_worksheet.pdf" target="_blank">worksheet</a> is an excellent starting point for finding words on which to do word studies.</p>

	<p>You might also try to read through your passage and list words for which you are unsure of the meaning, but which seem important.  In all cases, the following words and phrases should be researched.</p>

