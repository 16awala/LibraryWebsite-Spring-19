<h2 class="no-margin-top">Laptop Policy</h2>

    <h5 class="padding15-top no-margin-bottom">General Policy</h5>
        <ul class="padding25-left">
            <li>Laptops do not circulate overnight.</li>
            <li>The laptop software and procedures must be kept standardized so all users will know what to expect when they check them out.</li>
            <li>CD's and DVD's may be purchased in the lab office. There is no access to network drives except through NetStorage.</li>
        </ul>

    <h5 class="padding15-top no-margin-bottom">Borrowing Policy </h5>
        <ul class="padding25-left">
            <li>Only ONU enrolled students, faculty and staff with a valid ONU ID card plus a second form of photo identification can check out laptops during library hours until one hour before closing.</li>
            <li>The borrower agrees to follow this Laptop Policy.  </li>
            <li>The borrower agrees to treat this equipment with extreme care and to be aware of its location at all times. </li>
            <li>Laptop checkout privileges may be denied to people who damage the equipment, repeatedly return laptops late, with library fines/fees over $1, or do not follow these laptop use policies.  Lab supervisors do not have authorization to accept or override fines and/or fees.  They must be taken care of at Circulation Desk on the first floor.</li>
        </ul>

    <h5 class="padding15-top no-margin-bottom">Check-out Guidelines </h5>
        <ul class="padding25-left">
            <li>The borrower's ONU ID card and another photo ID will be used for verification of ONU affiliation. </li>
            <li>The maximum checkout period is two hours, but may be less.  Our system is now added to Benner Lab's circulation client; therefore if laptop is returned even one minute late, fines will be applied.</li>
            <li>To check out a laptop, ask the lab assistant if a laptop is available.</li>
            <li>If no laptops are available, ask to be put on the Laptop Waiting List. </li>
            <li>Laptops cannot be reserved ahead.</li>
            <li>Laptops must be returned directly to the Lab Assistant in the library computer lab office where they were checked out.</li>
            <li>Renewals for another two hours will depend on availability.</li>
        </ul>
    <h5 class="padding15-top no-margin-bottom">Conditions of Use </h5>
        <ul class="padding25-left">
            <li>The borrower agrees and understands that ONU and the Library assume no responsibility for loss or damage to borrower's personal property through use of the laptop.</li>
            <li>Laptops are to be used only in Benner Library. They may not be taken beyond the Library security gates or into the Fishbowl.</li>
            <li>DO NOT LEAVE A LAPTOP UNATTENDED.</li>
            <li>The borrower will report any equipment problems immediately to the Lab Assistant.</li>
        </ul>
    <h5 class="padding15-top no-margin-bottom">Overdue Fines </h5>
        <ul class="padding25-left">
            <li>The fine is $10.00 for the first (partial) hour overdue, and $5.00 per hour thereafter, until the computer is returned or declared lost.  Partial hours count as a full hour overdue.</li>
            <li>Overdue fines are non-refundable and will not be waived.</li>
            <li>The clock in the lab used for checkout will also be used for the check-in time.</li>
            <li>The borrower agrees to pay overdue charges for laptops not returned within the checkout period.</li>
        </ul>

    <h5 class="padding15-top no-margin-bottom">Other Charges</h5>
        <ul class="padding25-left">
            <li>The borrower agrees to be responsible for charges for damage based on actual repair costs.</li>
            <li>The borrower agrees to pay replacement cost for a lost or stolen laptop computer and/or other accessories. Replacement costs are approximately $1500 plus the accrued overdue charges.</li>
            <li>Replacement cost of a severely damaged, lost, or stolen laptop computer will be no less than $1,500 plus accrued overdue fines.</li>
        </ul>


