<h1>Circulation Desk</h1>
        
	<h4>Circulation Policies</h4>
		<p>In order to borrow materials, you must bring a valid ONU ID card with you to the Circulation Desk.  Curriculum Center Library materials are checked out at the CCL Circulation Desk, but books from the juvenile collection are checked out at the Circulation Desk on the main floor.  Some materials, such as reference books and periodicals, do not leave the library.
		</p>
	<h4>Reserve Materials</h4>
		<p>Materials are placed on reserve to make them available to all members of a class within a short time period.  Therefore, most reserve materials have circulation periods of only 2 hours. Please use the call number provided in your class syllabus when requesting reserve items at the Reserve Desk, or use the Reserve Book Room function of the BLIS Online Catalog to find that number.  Some reserve items, known as eReserve, are available online.
		</p>
	<h4>Loan Periods and Fines</h4>
		<p>All books in the regular collection check out for a 4-week period.  Some curriculum materials check out for 2 weeks.  A fine of $ .10 per day is charged for these overdue materials.<br />
		</p>
		
		<p>Reserve materials fall into three categories:  2-hour, 2-day, and 7-day.  Please return reserve materials promptly because the fines add up quickly.  The fine charged is $ .50 per day for 2-day and 7-day reserve and $ .25 <b>per hour</b> for 2-hour reserve.
		</p>
	<h4>Renewing Materials</h4>
		<p>Library materials can be renewed unless 1) the items are overdue, 2) the items are on reserve, 3) the renewal limit has been reached, or 4) the fine balance totals $1.00 or more.  To renew items, call 815-939-5354 or stop by the Circulation Desk.  You may also renew materials online:
		
		<ul class="padding10">
			<li>Go to the library's <a href="http://library.olivet.edu">homepage</a>.</li>
			<li>Click on BLISweb</li>
			<li>Click on Login in the upper right hand corner</li>
			<li>Enter your username and password (if you do not have an account, click Create New Account and enter the necessary information)</li>
			<li>Click on Checked Out items on the right hand side</li>
			<li>Click the Renew box next to each item you wish to renew</li>
			<li>Click the Renew Selected Items button at the bottom of the list</li>
			<li>Make sure that items have been renewed by checking the status column</li>
			<li>Not Renewed items must be returned to the library as soon as possible (If still needed, books can be reordered from another library)</li>
			<li>Books marked ILL on your account must be renewed through the Interlibrary Loan Department; call 815-928-5439</li>
		</ul>
		</p>	
	<h4>Returning Materials</h4>
		<p>There is a book return at the Circulation Desk and a book drop outside the building for the return of library books after hours. Items from the Curriculum Center Library must be returned to the  Circulation Desk.
		</p>
	<h4>Overdue Notices</h4>
		<p>Overdue notices are sent in this sequence: 2 days after material is due, 14 days, 30 days, and 48 days.  The notice at 48 days indicates both the fine and the replacement charge for the book. After 60 days, the replacement charge, the fine, and a $10 non refundable processing fee appear on the student's library record.
		</p>
	<h4>Security System</h4>
		<p>To prevent unauthorized removal of material, Benner Library has an electronic theft detection system.  Failure to properly check out materials will cause the alarm to sound.  The library is monitored by security cameras.
		</p>
	<h4>Telephones and Fax Services</h4>
		<p>There is a campus telephone located in the entry foyer of the library.  All other telephones in the library are for library personnel only.  Benner Library offers fee-based fax service.  For details, inquire at the main desk.
		</p>
		
