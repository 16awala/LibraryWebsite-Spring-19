<h1>Interlibrary Loan Policies</h1>

	<p><strong>Mission:</strong> Provide the right material to the right patron ASAP.</p>
	
	<p>
		<strong>Interlibrary Loan Patron Limitations:</strong> Benner Library Interlibrary Loan provides generous services for current students, staff, and faculty of Olivet Nazarene University, approximately 6000 patrons.  Services are also provided to patrons of our consortial partners ( i.e. students, staff, and faculty from 80+ I-Share institutions) as well as to libraries worldwide.<br /><br />
		The average cost of an interlibrary loan transaction is $22.  The university supports the expenses for these services so that, generally, there is no charge to the end user.<br /><br />
		In order to efficiently meet the needs of our patrons and comply with vendor restrictions and copyright law, some services are limited:  
	</p>

	<ul>
		<li>Any user may access our e-journals on site. However, interlibrary loan privileges for articles are strictly limited to current Olivet patrons and to requests between libraries.</li>
		<li>Interlibrary loan services are not available to alumni or to community patrons.  These library users may purchase a courtesy card for $10.00/year, which allows use of Benner Library returnable resources but does not extend to interlibrary loan privileges.  We encourage these patrons to use the interlibrary loan services of their local public libraries for items we cannot supply.</li>
		<li>Olivet patrons may not use our services to order for other patrons whether these patrons qualify for interlibrary loan services or not.</li>
	</ul>
	

	
