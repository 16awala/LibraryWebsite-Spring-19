<h2 class="header-line">Mac PDF Issues</h2>

		<p><strong>Opening a PDF on a Mac</strong></p>
	<p>	
	If you are using the Firefox web browser on a Mac and are unable to view PDFs, you have two options:
	</p>

		<ol>
			<li>Download the document and open it with the Preview program that comes with Mac OS</li>
			<li>Download the document and open it with <a href="http://get.adobe.com/reader/">Adobe PDF Reader</a></li>
		</ol>
		
	<p>
		Alternatively, you can use the built-in Safari browser which can open PDFs automatically, as long as you have the latest version of <a href="http://get.adobe.com/reader/">Adobe PDF Reader</a> installed.  
	</p>

	<p>	 
		If you are still having difficulties opening PDFs, please contact Benner Library or the IT Help Desk and provide as much detail as possible about the problem. 
	</p>
		

