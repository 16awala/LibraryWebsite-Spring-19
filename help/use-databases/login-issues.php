<h2 class="header-line">Login Issues</h2>

<h4>Accessing Library Databases</h4>

        <ul>
			<li>Go to the Benner Library home page at <a href="http://library.olivet.edu">http://library.olivet.edu</a>.</li>
			<li>Under the heading "Journals &amp; Articles," click on "eResources A-Z."</li>
			<li>Click on the name of a database that you want to search.</li>
			<li>If you are off campus, the login page will pop up.</li>
			<li>Enter your campus network username and <a href="/help/use-databases/password-issues.php" target="_blank">password</a> when you are prompted for them.</li>

            <blockquote>
                <ul>
                    <li>Your username is your ONUnet username (same as My Olivet).</li>
                    <li>Your password is your ONUnet password (same as My Olivet).</li>
                </ul>
            </blockquote>

			<li>Security warnings may pop up - answer in the affirmative within older browsers and in the negative within newer browsers.</li>
			<li>Once you've been authenticated, the database search page should display.  Enter your search terms in the boxes provided.</li>
    	</ul>

<h4>To Get Help</h4>
        <ul>
			<li>For trouble with passwords, go to the <a href="http://changemypassword.olivet.edu/" target="_blank">http://changemypassword.olivet.edu</a> site and follow the prompts there.</li>
			<li>If your ONUnet username and password still do not work or you do not know what they are, call the IT help desk at 815-939-5302. They can reset your password for you.</li>
			<li>If you are logged in but having trouble accessing a particular article or subject, contact the Reference Desk (815-939-5355) or Interlibrary Loan (815-928-5439). They can help you with your research technique or give you additional alternatives.</li>
			<li>If you have any problems particular to distance education (graduate or continuing undergraduate) research, please contact the <a href='/forms/email/index.php?id=002'>SGCS Librarians</a>.</li>
    	</ul>
	</p>


