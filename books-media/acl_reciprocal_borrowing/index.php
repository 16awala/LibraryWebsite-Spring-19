<h1>Association of Christian Librarians Reciprocal Borrowing Program</h1>

	<p>
		<strong>What is it?</strong><br />
		The Association of Christian Librarians (ACL) Reciprocal Borrowing Program enables eligible ONU patrons to have in-person borrowing privileges at other ACL-affiliated participating libraries.<br /><br />

		<strong>Eligible Patrons</strong><br />
		Currently employed faculty and staff at ONU as well as currently enrolled students in good standing are eligible.<br /><br />

		<strong>How to Borrow</strong><br />
		Borrower obtains a signed
		<a href="http://www.acl.org/index.cfm/christian-library-consortium/reciprocal-borrowing-program/rbp-authorization-form/" target="_blank">Authorization Form</a>
		from the home library to receive borrowing privileges from a participating library. The home library has the authority to issue or deny this privilege. <br /><br />

		Borrower takes the signed Authorization Form along with a photo ID to a participating library.  That library collects the form and any additional contact information needed from the borrower in order to create a library record for the patron. Privileges and loan periods are determined by the lending library and may vary from those of the borrower’s home institution.<br /><br />

		Borrower returns materials to the lending library by the requested due date and pays any applicable fines and fees for late, lost, or damaged materials as determined by the lending library.<br /><br />

		<strong>Participating Libraries</strong><br />
		As of June 2017 there were 4 participating libraries located within Olivet’s educational region:<br /><br />

		Grace College & Seminary – Winona Lake, IN<br />
		Indiana Wesleyan University – Marion, IN<br />
		Puritan Reformed Theological Seminary – Grand Rapids, MI<br />
		Trinity International University – Deerfield, IL<br /><br />

		Click here for a complete list of participating libraries: <a href="http://www.acl.org/RBP" target="_blank">http://www.acl.org/RBP</a>
	</p>



