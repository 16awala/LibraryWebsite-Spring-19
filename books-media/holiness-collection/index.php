<h1 class="center">Holiness Collection</h1>

<p class="center italic">We can only remember who we are if we remember where we have come from.</p>

<p>The Holiness Collection is a collaborative initiative between Olivet Nazarene Universityâ€™s Benner Library and the School of Theology and Christian Ministry that will preserve the printed record of the Wesleyan Holiness movement in the United States and Canada. This collection is still in the process of being built but it will consist of a wide variety of printed material produced by the holiness movement, including formal theological texts and popular tracts.  The goal of the Holiness Collection is to assemble as complete a selection of these materials as possible, to retain them in a secure site, and to make them available to students and researchers who will value access to this rich range of holiness materials. In order to not risk having certain materials lost to future readers and researchers some collection documents may not be available for regular library circulation.</p>

<p>For more information about the Holiness Collection, please contact <a href="/forms/email/index.php?id=023">Kyle Olney</a>.

