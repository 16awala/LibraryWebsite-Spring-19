<h1>One Book, One Community (OBOC)</h1>

<p>The <a href="http://kalamembers.wordpress.com/" target="_target">Kankakee Area Library Association</a> (KALA) has brought together libraries of many types: school, college, public, and special within Kankakee County and surrounding areas for more than ten years. The mission of KALA is to promote cooperation among libraries and enhance library services for our patrons, students, and communities. KALA meets regularly to discuss library issues, bring library training to our area, and work on collaborative projects including various grants and literacy projects.<br /><br />

	One Book, One Community is an annual project of KALA that began in 2003.<br /><br />

	During the month of October, everyone in a community is invited to read and discuss the same book. This concept initiated in 1998 in Seattle, Washington, and has since spread across the nation to hundreds of libraries. It is our hope that local One Book, One Community activities will encourage individuals across the greater Kankakee area to connect through a shared reading experience.<br /><br />

